#include "obstacle_map.h"
#include <iostream>

GridMap::GridMap(const Eigen::MatrixXi& grid)
    :grid_(grid), rows_(grid.rows()), cols_(grid.cols()) {}


bool GridMap::isFree(int x, int y) const
{
    return x >= 0 && x < rows_ && y >= 0 && y < cols_ && grid_(x, y) == 0;
}

int GridMap::rows() const { return rows_; }
int GridMap::cols() const { return cols_; }

void GridMap::print(const std::vector<std::pair<int ,int>>& path) const
{
    Eigen::MatrixXi disp = grid_;
    for(const auto& p : path)
    {
        int x = p.first, y = p.second;
        if (x >= 0 && x < rows_ && y >= 0 && y < cols_)
        {
            disp(x, y) = 2;
        }   
    }
    for(int i = 0; i < rows_; ++i)
    {
        for (int j = 0; j < cols_; ++j)
        {
            if (disp(i, j) == 1) 
            {
                std::cout << "██";
            }
            else if (disp(i, j) == 2)
            {
                std::cout << "**";
            }
            else
            {
                std::cout << "  ";
            }
        }
        std::cout<<"\n";    //换行
    }
}

